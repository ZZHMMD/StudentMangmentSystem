package com.zzh.bean;

public class GradeForm {
	
	private String id;
	
	private String studyyear;
	
	private String studyseason;
	
	private String math;
	
	private String english;
	
	private String  javatest;
	
	private double totalgrade;
	
	private String studentid;
	
	

	public String getStudentid() {
		return studentid;
	}

	public void setStudentid(String studentid) {
		this.studentid = studentid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStudyyear() {
		return studyyear;
	}

	public void setStudyyear(String studyyear) {
		this.studyyear = studyyear;
	}

	public String getStudyseason() {
		return studyseason;
	}

	public void setStudyseason(String studyseason) {
		this.studyseason = studyseason;
	}

	public String getMath() {
		return math;
	}

	public void setMath(String math) {
		this.math = math;
	}

	public String getEnglish() {
		return english;
	}

	public void setEnglish(String english) {
		this.english = english;
	}

	public String getJavatest() {
		return javatest;
	}

	public void setJavatest(String javatest) {
		this.javatest = javatest;
	}

	public double getTotalgrade() {
		return totalgrade;
	}

	public void setTotalgrade(double totalgrade) {
		this.totalgrade = totalgrade;
	}
	
	

}
